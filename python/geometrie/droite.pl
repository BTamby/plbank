Une équation de droite est de la forme $$y = ax+b$$ si elle n'est pas verticale.

Ecrire une fonction **equation(x1,y1,x2,y2)** qui retourne un tuple contenant les valeurs a et b de l'équation de droite passant par les deux points (x1,y1) et (x2,y2).

	>>> equation(1,0,0,1)
	(1.0,-1.0)
	



